package Lines;

public class Line {
    int javaLine;
    String type;
    int solStart;
    int solEnd;

    public Line(int javaLine, String type, int solStart, int solEnd){
        this.javaLine = javaLine;
        this.type = type;
        this.solStart = solStart;
        this.solEnd = solEnd;
    }
}
